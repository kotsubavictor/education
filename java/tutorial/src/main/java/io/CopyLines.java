package io;

import java.io.*;

/**
 * @see BufferedReader
 */
public class CopyLines {
    public static void main(String[] args) throws IOException {

        BufferedReader inputStream = null;
        PrintWriter outputStream = null;

        try {
            inputStream = new BufferedReader(new FileReader("./tutorial/src/main/resources/io/xanadu.txt"));
            outputStream = new PrintWriter(new FileWriter("./tutorial/src/main/resources/io/characteroutput.txt"));

            String l;
            while ((l = inputStream.readLine()) != null) {
                outputStream.println(l);
            }
        } finally {
            if (inputStream != null) {
                inputStream.close();
            }
            if (outputStream != null) {
                outputStream.close();
            }
        }
    }
}
